RESULT do_ecc_test(void)
{
    Certificate c;
    Cert cert = &c;
    RESULT r = parse_cert( (BYTE*) yh_servcert,
                   sizeof(yh_servcert), cert );
    if( r != OK )
    {
        printf("parse_cert failed with [%X] \n", (SHORT) r);
        return r;
    }
    r = asn_check_verify_sig( cert );
    printf("Result %X [%s]\n", (SHORT) r, (r == OK)? "OK":"Failed" );
    return r;
}

