unsigned char *test_aes_encrypt(EVP_CIPHER_CTX *e, unsigned char *plaintext, int *len)
{
    /* max ciphertext len for a n bytes of plaintext is n + AES_BLOCK_SIZE -1 bytes */
    int c_len = *len + CIPH_BLOCK_SZ, f_len = 0;
    unsigned char *ciphertext = malloc(c_len);
    int x;
    /* allows reusing of 'e' for multiple encryption cycles */
    x = EVP_EncryptInit_ex(e, NULL, NULL, NULL, NULL);
    if( !x )
    {
        printf( "EVP_EncryptInit_ex [failed]\n" );
    }
    /* update ciphertext, c_len is filled with the length of ciphertext generated,
    *len is the size of plaintext in bytes */
    x = EVP_EncryptUpdate(e, ciphertext, &c_len, plaintext, *len);
    if( !x )
    {
        printf( "EVP_EncryptUpdate [failed]\n" );
    }
    /* update ciphertext with the final remaining bytes */
    x = EVP_EncryptFinal_ex(e, ciphertext+c_len, &f_len);
    if( !x )
    {
        printf( "EVP_EncryptFinal_ex [failed]\n" );
    }
    EVP_CIPHER_CTX_cleanup(e);
   *len = c_len + f_len;
    return ciphertext;
}
/*
 * Decrypt *len bytes of ciphertext
 */
unsigned char *test_aes_decrypt(EVP_CIPHER_CTX *e, unsigned char *ciphertext, int *ciphertext_len)
{
    int bytes_written = *ciphertext_len;
    int plaintext_len = 0;
    int x;
    unsigned char *plaintext = malloc(*ciphertext_len);
    x = EVP_DecryptInit_ex(e, NULL, NULL, NULL, NULL);
    if( !x )
    {
        printf( "EVP_DecryptInit_ex [failed]\n" );
    }
    x = EVP_DecryptUpdate(e, plaintext, &bytes_written, ciphertext, *ciphertext_len);
    if( !x )
    {
        printf( "EVP_DecryptUpdate [failed]\n" );
    }
    plaintext_len += bytes_written;
    x = EVP_DecryptFinal_ex(e, plaintext + bytes_written, &bytes_written);
    if( !x )
    {
        printf( "EVP_DecryptFinal_ex [failed]\n" );
    }
    plaintext_len += bytes_written;
    EVP_CIPHER_CTX_cleanup(e);
    return plaintext;
}
