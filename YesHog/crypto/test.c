int test_aes_clobber_input(void)
{
    char msg[] = "So I Dub Thee Somewhat Forgiven";
    printf( "Original message [%s]\n", msg );
    BYTE inkey[16] = { 0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
                       0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c };
    BYTE mkey[CIPH_KEY_SZ];
    aes_expand_key( inkey, mkey );
    char buf[ 128 ];
    char* c = buf;
    bzero( buf, sizeof( buf ) );
    memcpy( buf, msg, strlen(msg) );
    BYTE numBlocks = (strlen(msg) >> 4) + 1;
    BYTE i = 0;
    for( ; i < numBlocks; i++ )
    {
        aes_encrypt( (BYTE*) c, mkey, (BYTE*) c );
        c += CIPH_BLOCK_SZ;
    }
    printf( "Clobber encrypted\n" );
    hexdump( (BYTE*) buf, (numBlocks << 4) );
    c = buf;
    for( i = 0; i < numBlocks; i++ )
    {
        aes_decrypt( (BYTE*) c, mkey, (BYTE*) c );
        c += CIPH_BLOCK_SZ;
    }
    printf( "Clobber decrypted [%s]\n", buf );
    return OK;
}
int test_aes_cbc_enc(void)
{
    BYTE key[] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
                  0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c};
    /* EVP_CIPHER_CTX_init(&de) somehow screws up the key, make
       a copy until we figure why */
    BYTE k1[] =  {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
                  0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c};
    BYTE iv[] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
                 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c };
    char plaintext[256];
    bzero(plaintext, sizeof(plaintext) );
    strcpy(plaintext, "all in all just another brick in the wall");
    /* initialize openssl's ctx */
    EVP_CIPHER_CTX en;
    EVP_CIPHER_CTX_init(&en);
    EVP_EncryptInit_ex(&en, EVP_aes_128_cbc(), NULL, key, iv);
    /* encrypt using openssl */
    int len = strlen(plaintext) + 1;
    int ylen = len;
    unsigned char* ciph = test_aes_encrypt( &en, (BYTE*) plaintext, &len );
    printf( "openssl enc\n" );
    hexdump( ciph, len );
    /* initialize yh_crypto aes */
    BYTE out[256];
    SHORT l = aes_cbc_encrypt( iv, key, (BYTE*) plaintext, ylen,
                                    out, sizeof(out) );
    if( l != len || memcmp( out, ciph, l ) )
    {
        printf( "Encryption failed perhaps padding is TLS\n" );
    }
    printf( "yh cbc encrypted\n" );
    hexdump( out, l );
    l = aes_cbc_decrypt( iv, key, out, l );
    printf( "yh decrypt [%d] [%s]\n", l, out );
    /* decrypt using openssl */
    EVP_CIPHER_CTX de;
    EVP_CIPHER_CTX_init(&de);
    EVP_DecryptInit_ex(&de, EVP_aes_128_cbc(), NULL, k1, iv);
    char* decr = (char*) test_aes_decrypt( &de, ciph, &len );
    printf( "Openssl decrypt [%s]\n", decr );
    if( strncmp( (char*) out, decr, strlen( plaintext ) != 0 ) )
    {
        if( ciph ) free( ciph );
        if( decr ) free( decr );
        return -3;
    }
    if( ciph ) free( ciph );
    if( decr ) free( decr );
    return OK;
}
